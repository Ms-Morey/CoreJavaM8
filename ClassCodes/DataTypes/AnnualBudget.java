class AnnualBudget 
{
    public static void main(String[] args)
{
        long educationBudget = 500000000000L;      // 500B
        long healthcareBudget = 300000000000L;     // 300B
        long defenseBudget = 700000000000L;        // 700B
        long infrastructureBudget = 400000000000L; // 400B
        long totalBudget = 2000000000000L;         // 2T

        System.out.println("Education Budget: " + educationBudget);
        System.out.println("Healthcare Budget: " + healthcareBudget);
        System.out.println("Defense Budget: " + defenseBudget);
        System.out.println("Infrastructure Budget:" + infrastructureBudget);
        System.out.println("Total Budget:" + totalBudget);
    }
}
